	<h4>1. 収集する会員情報</h4>
	<p>本ポリシーにおける「会員情報」とは、会員の識別にかかる情報、通信サービス上の行動履歴、会員端末に関連して生成または蓄積された情報で、本ポリシーに基づき当社が収集するものとします。<br />
	本サービスにおいて当社が収集する会員情報は、以下のようなものとなります。</p>
	<ol>
		<li>会員からご提供いただく、氏名、メールアドレス、その他当社が定める入力フォームに会員が入力する情報。</li>
		<li>会員が本サービスを利用するにあたって、他サービスと連携を許可することにより、当該他サービスからご提供いただく情報。</li>
		<li>会員が本サービスを利用するにあたって、当社が本サービスへのアクセス状況やその他ご利用方法から収集する情報。これには端末情報、ログ情報、Cookieが含まれます。</li>
	</ol>

