		<li>その他、個人情報保護法その他の法令で認められる場合</li>
	</ol>

	<h4>4. 個人情報の訂正削除</h4>
	<p>個人情報の訂正は任意となっています。ただし、情報に漏れやミスがある場合、適切なお問い合わせへの回答ができない場合があります。<br />
	また会員により、個人情報の削除または利用停止を求められた場合には、ご本人からのご請求であることを確認の上で遅滞なく必要な調査を行い、その結果に基づいて個人情報の内容の削除または利用停止を行い、その旨を会員に通知します。<br />
	なお、合理的な理由に基づいて削除または利用停止を行わない旨の決定をしたときは、会員に対しその旨を通知します。</p>

