		<li>本サービスの提供、維持、保護及び改善のため</li>
		<li>本サービスに関するご案内、お問い合わせ等への対応のため</li>
		<li>本サービスに関する当社の規約、ポリシー等（以下「規約等」）に違反する行為への対応のため</li>
		<li>本サービスに関する規約等の変更などを通知するため</li>
		<li>当社のサービスに関連して、個人を特定できない形式に加工した統計データを作成するため</li>
		<li>当社または第三者の広告配信または表示のため</li>
		<li>その他マーケティングに利用するため</li>
		<li>上記の利用目的に付随する利用目的のため</li>
	</ol>

	<h4>3. 第三者提供</h4>
	<p>当社は、個人情報について、個人情報保護法その他の法令に基づき開示が認められる場合を除くほか、あらかじめ会員の同意を得ないで、第三者（個人情報の保護に関する法律第23条に定める「第三者」をいいます）に提供しません。
